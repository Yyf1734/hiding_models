class MultiboxLoss(nn.Module):
    def __init__(self, priors=config.priors, iou_threshold=0.5, neg_pos_ratio=3,
                             center_variance=0.1, size_variance=0.2):
        """Implement SSD Multibox Loss.

        Basically, Multibox loss combines classification loss
         and Smooth L1 regression loss.
        """
        super(MultiboxLoss, self).__init__()
        self.iou_threshold = iou_threshold
        self.neg_pos_ratio = neg_pos_ratio
        self.center_variance = center_variance
        self.size_variance = size_variance
        self.priors = priors

    def forward(self, confidence, predicted_locations, labels, gt_locations):
        """Compute classification loss and smooth l1 loss.

        Args:
            confidence (batch_size, num_priors, num_classes): class predictions.
            locations (batch_size, num_priors, 4): predicted locations.
            labels (batch_size, num_priors): real labels of all the priors.
            boxes (batch_size, num_priors, 4): real boxes corresponding all the priors.
        """
        # print(gt_locations.shape[0])
        num_classes = confidence.size(2)
        with torch.no_grad():
            # derived from cross_entropy=sum(log(p))
            loss = -F.log_softmax(confidence, dim=2)[:, :, 0]
            mask = box_utils.hard_negative_mining(loss, labels, self.neg_pos_ratio)
        pos_mask = labels > 0
        gt_boxes = box_utils.convert_locations_to_boxes(gt_locations.to(torch.device('cpu')), self.priors.to(torch.device('cpu')), self.center_variance,self.size_variance)
        predicted_boxes = box_utils.convert_locations_to_boxes(predicted_locations.to(torch.device('cpu')), self.priors.to(torch.device('cpu')), self.center_variance,self.size_variance)
        iou = box_utils.iou_of(gt_boxes[pos_mask, :], predicted_boxes[pos_mask, :])
        confidence = confidence[mask, :]
        # print(labels[mask].shape)
        # print(torch.argmax(confidence.reshape(-1, num_classes), dim=1).shape)
        predict_label = torch.argmax(confidence.reshape(-1, num_classes), dim=1)
        total = predict_label.shape[0]
        correct = sum(predict_label == labels[mask])
        classification_loss = F.cross_entropy(confidence.reshape(-1, num_classes), labels[mask], size_average=False)
        pos_mask = labels > 0
        predicted_locations = predicted_locations[pos_mask, :].reshape(-1, 4)
        gt_locations = gt_locations[pos_mask, :].reshape(-1, 4)
        # print(predicted_locations.shape)
        smooth_l1_loss = F.smooth_l1_loss(predicted_locations, gt_locations, size_average=False)
        num_pos = gt_locations.size(0)
        return smooth_l1_loss/num_pos, classification_loss/num_pos #, iou.max()
